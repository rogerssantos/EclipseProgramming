
public class Calculo {
	static double soma(double a, double b) {
	    double resposta = a+b;
	    return resposta;          
	}

	static double diminui(double a, double b) {
	    double resposta = a-b;
	    return resposta;          
	}

	static double multiplica(double a, double b) {
	    double resposta = a*b;
	    return resposta;          
	}

	static double divide(double a, double b) {
	    double resposta = a/b;
	    return resposta;
	}
}